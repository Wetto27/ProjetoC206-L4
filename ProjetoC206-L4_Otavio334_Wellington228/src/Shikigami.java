public class Shikigami {

    private String nome;
    private boolean exorcizado;
    private boolean totalidade;
    private boolean destruido;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public boolean isExorcizado() {
        return exorcizado;
    }

    public void setExorcizado(boolean exorcizado) {
        this.exorcizado = exorcizado;
    }

    public boolean isTotalidade() {
        return totalidade;
    }

    public void setTotalidade(boolean totalidade) {
        this.totalidade = totalidade;
    }

    public boolean isDestruido() {
        return destruido;
    }

    public void setDestruido(boolean destruido) {
        this.destruido = destruido;
    }
}
